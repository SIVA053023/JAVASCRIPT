// write a programme to check if a number is 3 digit number or not ?
let num=456;
if(num>=99 && num<=999){
    console.log(num,"=It is a 3 digit number")   // output:- 456=It is a 3 digit number
}
else{
    console.log(num,"=It is not a 3 digit number")
}

